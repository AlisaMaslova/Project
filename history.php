        сприяння відродженню та популяризації хорового мистецтва в Україні.</p>

            <p>Самобутності творчого портрету хору сприяє яскравість та різноманітність 

        його репертуару - це й класична та духовна музика, й обробки народних пісень, 

        і сучасні хорові композиції. Завдяки співпраці колективу з композиторами Харківщини, 

        дитячий хор став своєрідною творчою лабораторією багатьох митців та мав змогу 

        першим виконувати твори М. Кармінського, В. Птушкіна, І. Гайденка, В. Мужчиля та інших. </p>

