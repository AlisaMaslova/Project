        <p>Із 1990-го року хором керує його вихованка, кандидат мистецтвознавства Юлія Іванова. 

        За свою багаторічну історію колектив неодноразово гастролював Європою, 

        завдяки чому став знаним поза межами України та отримав звання Народного художнього колективу.

        Хор підтримує традиції кращих дитячих колективів України, він є творчою лабораторією харківської

        спілки композиторів України. Виступи колективу проходять у кращих концертних залах міста:

        у театрі опери та балету ім. М.Лисенка, харківській філармонії, у Великій залі Харківського 

        національного університету мистецтв ім. І.П.Котляревського</p>

            <p>Навчання у колективі здійснюється за власною програмою, яка за своїм змістом наближена 

        до програми хорової студії. Навчальна програма хору має на меті формування музично-теоретичних 

        та виконавських навичок та передбачає як групові, так й індивідуальні методи роботи. 

        Про успішне втілення програмних вимог свідчить висока вокальна культура виконання, яка, 

        окрім технічної досконалості, характеризується особливою виразністю, емоційністю звучання. 

        Ґрунтовний підхід до музично-професійної підготовки дає змогу вихованцям продовжувати свій 

        творчий шлях у мистецьких навчальних закладах – випускники колективу щороку поповнюють ряди 

        вступників на хормейстерські та вокальні відділення музичних і педагогічних училищ, університетів, 

        академій культури та мистецтв.</p>

            <p>У колективі працюють чотири різновікові групи – підготовча, молодша, середня та старша, що дає змогу 

        дотримуватись принципу логічності й послідовності навчання. Освітній процес у кожній 

        з груп базується, перш за все, на врахуванні вікових та індивідуальних особливостей дитини. </p>

