        <p>Народний художній колектив хор «Скворушка» був заснований у  1965 році при 

        Харківському обласному Палаці дитячої та юнацької творчості. 

        Ініціатором створення колективу, а також його першим керівником стала відома хормейстерка Лідія Шапіро.

        Вже з перших років свого існування колектив заявив про себе як художній колектив з неповторним,

        яскравим обличчям. за роки творчої діяльності, під керівником Л.Шапіро, хор став лауреатом

        Всесоюзних та Республіканських конкурсів та фестивалів дитячої самодіяльної творчості, 

        здобув звання "Зразковий", багато концертував в нашій країні та за кордоном.</p>

            <p>Колектив пройшов творчий шлях - від простих піонерських пісень до складного духовного,

        класичного та сучасного авангардного репертуару. Репертуар хору, його напрям передбачає високий

        професійний рівень кожного хориста, володіння комплексом музично-телретичних знань.</p>

        <figure class="sign1">

        <p><img src="/pictures/history/ivanova.jpg" align="right"></p>

